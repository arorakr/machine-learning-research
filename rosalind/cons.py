def build_matrix(dna_strings):
  matrix = []
  current_sequence = []
  first = True
    
  for line in dna_strings.splitlines():
    if line.startswith('>'):
        if not first:
          matrix.append(''.join(current_sequence))
        first = False
        current_sequence = []
    else:
        current_sequence.append(line.strip())

  matrix.append(''.join(current_sequence))

  return matrix

def cons():
  matrix = build_matrix(dna_strings)
  As = []
  Cs = []
  Gs = []
  Ts = []

  for col in range(len(matrix[0])):
    A, C, G, T = 0, 0, 0, 0

    for row in range(len(matrix)):
      if matrix[row][col] == 'A': A += 1
      if matrix[row][col] == 'C': C += 1
      if matrix[row][col] == 'G': G += 1
      if matrix[row][col] == 'T': T += 1

    As.append(A)
    Cs.append(C)
    Gs.append(G)
    Ts.append(T)

  consensus = []

  for index in range(len(As)):
    bigger = ['A', As[index]]
    if Cs[index] > bigger[1]: bigger = ['C', Cs[index]]
    if Gs[index] > bigger[1]: bigger = ['G', Gs[index]]
    if Ts[index] > bigger[1]: bigger = ['T', Ts[index]]
    consensus.append(bigger[0])

  print(''.join(consensus))
  print('A:', ' '.join(map(str, As)))
  print('C:', ' '.join(map(str, Cs)))
  print('G:', ' '.join(map(str, Gs)))
  print('T:', ' '.join(map(str, Ts)))
